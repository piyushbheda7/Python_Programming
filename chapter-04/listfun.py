l = [1, 8, 7, 2, 21, 15]

# sort() function to sort the elements of list
l.sort()
print(l)

# reverse() function to reverse the elements of list
l.reverse()
print(l)

# append(no) function to add no at last position
l.append(10)
print(l)

# insert(3,8) function to add 8 at index 3
l.insert(3, 4)
print(l)

# pop(2) function to delete the element at index 2 and return its value
print(l.pop(2))

# remove(21) fuction to remove 21 element 
l.remove(21)
print(l)